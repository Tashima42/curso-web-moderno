/*
03) Crie uma função que recebe dois parâmetros, base e expoente, e retorne a base elevada ao expoente
*/

function elevarBase(base, expoente) { 
    return (base ** expoente)
 }

 console.log(elevarBase(2,2))
 console.log(elevarBase(2,3))
 console.log(elevarBase(3,2))
 console.log(elevarBase(9,2))